#include <iostream>
using namespace std;

int main() {
    // Оголошення змінної для зберігання числа
    int number;

    // Запит на введення числа від користувача
    cout << "Введіть ціле число: ";
    cin >> number;

    // Перевірка, чи є число парним
    if (number % 2 == 0) {
        cout << "Число парне: ТАК" << endl;
    } else {
        cout << "Число парне: НІ" << endl;
    }

    // Перевірка, чи ділиться число на 8
    if (number % 8 == 0) {
        cout << "Число ділиться на 8: ТАК" << endl;
    } else {
        cout << "Число ділиться на 8: НІ" << endl;
    }

    // Перевірка, чи ділиться число на 16
    if (number % 16 == 0) {
        cout << "Число ділиться на 16: ТАК" << endl;
    } else {
        cout << "Число ділиться на 16: НІ" << endl;
    }

    // Виведення числа у вісімковому форматі
    cout << "Число у вісімковому форматі: " << oct << number << endl;

    // Виведення числа у шістнадцятковому форматі
    cout << "Число у шістнадцятковому форматі: " << hex << number << endl;

    return 0;
}
