#include <iostream>
using namespace std;

int main() {
    int m, n, k;

    // Зчитування вхідних даних
    cout << "Введіть кількість рядків (m): ";
    cin >> m;
    cout << "Введіть кількість стовпців (n): ";
    cin >> n;
    cout << "Введіть товщину межі (k): ";
    cin >> k;

    // Перевірка на коректність введених даних
    if (m < 1 || m > 100 || n < 1 || n > 100 || k < 1 || k >= m || k >= n) {
        cout << "Неправильні вхідні дані! Перевірте умови для m, n та k." << endl;
        return 1; // Завершення програми з помилкою
    }

    // Виведення прямокутника з межами товщиною k
    for (int i = 0; i < m; i++) { // Цикл для рядків
        for (int j = 0; j < n; j++) { // Цикл для стовпців
            // Перевірка, чи поточний символ знаходиться в межах
            if (i < k || i >= m - k || j < k || j >= n - k) {
                cout << "*";
            } else {
                cout << " ";
            }
        }
        cout << endl; // Перехід на новий рядок після завершення рядка
    }

    return 0;
}
