#include <iostream>
#include <iomanip> // для використання setw
using namespace std;

int main() {
    int m, n;

    // Зчитування вхідних даних
    cout << "Введіть кількість рядків (m): ";
    cin >> m;
    cout << "Введіть кількість стовпців (n): ";
    cin >> n;

    // Перевірка на коректність введених даних
    if (m < 0 || m > 100 || n < 0 || n > 100) {
        cout << "Неправильні вхідні дані! m і n повинні бути в діапазоні від 0 до 100." << endl;
        return 1; // Завершення програми з помилкою
    }

    // Створення двовимірного масиву
    int arr[100][100]; // Максимальний розмір 100x100

    // Заповнення масиву у вигляді змійки
    int number = 0; // Початкове значення числа
    for (int i = 0; i < m; i++) { // Цикл для рядків
        if (i % 2 == 0) { // Якщо рядок парний (зліва направо)
            for (int j = 0; j < n; j++) {
                arr[i][j] = number++;
            }
        } else { // Якщо рядок непарний (справа наліво)
            for (int j = n - 1; j >= 0; j--) {
                arr[i][j] = number++;
            }
        }
    }

    // Виведення масиву
    for (int i = 0; i < m; i++) {
        for (int j = 0; j < n; j++) {
            cout << setw(3) << arr[i][j]; // Виведення числа з фіксованою шириною
        }
        cout << endl; // Перехід на новий рядок після завершення рядка
    }

    return 0;
}
