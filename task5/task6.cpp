#include <iostream>
#include <string>

std::string numberToWords(int n) {
    if (n == 0) return "нуль"; // Обробка випадку, коли n = 0

    // Масиви слів для чисел
    std::string units[] = {"", "один", "два", "три", "чотири", "п'ять", "шість", "сім", "вісім", "дев'ять"};
    std::string teens[] = {"десять", "одинадцять", "дванадцять", "тринадцять", "чотирнадцять", "п'ятнадцять", "шістнадцять", "сімнадцять", "вісімнадцять", "дев'ятнадцять"};
    std::string tens[] = {"", "", "двадцять", "тридцять", "сорок", "п'ятдесят", "шістдесят", "сімдесят", "вісімдесят", "дев'яносто"};
    std::string hundreds[] = {"", "сто", "двісті", "триста", "чотириста", "п'ятсот", "шістсот", "сімсот", "вісімсот", "дев'ятсот"};

    std::string result;

    // Обробка сотень
    if (n >= 100) {
        result += hundreds[n / 100] + " ";
        n %= 100;
    }

    // Обробка десятків
    if (n >= 20) {
        result += tens[n / 10] + " ";
        n %= 10;
    } else if (n >= 10) {
        result += teens[n - 10] + " ";
        n = 0; // Числа від 10 до 19 обробляються окремо
    }

    // Обробка одиниць
    if (n > 0 && n < 10) {
        result += units[n] + " ";
    }

    // Видалення зайвого пробілу в кінці
    if (!result.empty() && result.back() == ' ') {
        result.pop_back();
    }

    return result;
}

int main() {
    int n;
    std::cout << "Введіть число (n ≤ 1000): ";
    std::cin >> n;

    if (n < 0 || n > 1000) {
        std::cout << "Число має бути в діапазоні від 0 до 1000.\n";
        return 1;
    }

    std::cout << "Число " << n << " словами: " << numberToWords(n) << "\n";
    return 0;
}
